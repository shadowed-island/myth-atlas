import fs from "node:fs";
import path from "node:path";

import { loadStories } from "./load-stories.mjs";

const workspaceDir = process.cwd();
const outputPath = path.join(workspaceDir, "docs/story-narrative-audit-checklist.md");
const generatedPath = path.join(workspaceDir, "lib/story-narratives.generated.ts");
const curatedPath = path.join(workspaceDir, "lib/story-narratives.curated.json");

const stories = loadStories(workspaceDir);
const generated = readGeneratedNarratives(generatedPath);
const curated = fs.existsSync(curatedPath)
  ? JSON.parse(fs.readFileSync(curatedPath, "utf8"))
  : {};

const zhBoilerplate = [
  "先朝着",
  "很快也跟上了这条路",
  "随着前行一点点变大",
  "就在转折前等着众人",
  "没有停下，而是继续往里走",
  "把中段的局面越收越紧",
  "真正的险境",
  "直到一次动作打破僵局",
  "所有人的去路都变了样",
  "被留在后面的人也只能继续面对这件事的后果",
  "讲述就停在这个结果上"
];

const enBoilerplate = [
  "first moves toward",
  "follows on the same road",
  "grows as the way narrows",
  "waits at the turning point",
  "presses on instead of stopping",
  "tighten around the middle of the struggle",
  "turns the next step into open danger",
  "One act finally breaks the deadlock",
  "path changes shape for everyone involved",
  "Those left behind must live with what happened there",
  "The last scene settles on that result"
];

const collator = new Intl.Collator("zh-Hans");

const rows = stories
  .map((story) => {
    const paragraphs = story.narrativeParagraphs ?? curated[story.id] ?? generated[story.id];
    const zhParagraphs = paragraphs?.zh ?? [];
    const enParagraphs = paragraphs?.en ?? [];
    const zhSentences = zhParagraphs.flatMap((paragraph) => splitParagraph(paragraph, "zh"));
    const enSentences = enParagraphs.flatMap((paragraph) => splitParagraph(paragraph, "en"));
    const joinedZh = zhParagraphs.join("\n");
    const joinedEn = enParagraphs.join("\n");
    const issues = [];

    if (zhParagraphs.length !== 3 || enParagraphs.length !== 3) {
      issues.push("paragraph-count");
    }
    if (zhSentences.length !== 12 || enSentences.length !== 12) {
      issues.push("sentence-count");
    }
    if (zhBoilerplate.some((phrase) => joinedZh.includes(phrase))) {
      issues.push("zh-boilerplate");
    }
    if (enBoilerplate.some((phrase) => joinedEn.includes(phrase))) {
      issues.push("en-boilerplate");
    }
    if (!paragraphs) {
      issues.push("missing-narrative");
    }

    return {
      id: story.id,
      region: story.region?.zh ?? "未分区",
      country: story.country.zh,
      title: story.title.zh,
      titleEn: story.title.en,
      source: story.narrativeParagraphs
        ? "manual"
        : curated[story.id]
          ? "curated"
          : "generated",
      synthetic: isSyntheticSeed(story),
      issues
    };
  })
  .sort((left, right) =>
    collator.compare(left.region, right.region)
    || collator.compare(left.country, right.country)
    || collator.compare(left.title, right.title)
  );

const total = rows.length;
const checked = rows.filter((row) => row.issues.length === 0).length;
const sources = {
  manual: rows.filter((row) => row.source === "manual").length,
  curated: rows.filter((row) => row.source === "curated").length,
  generated: rows.filter((row) => row.source === "generated").length
};
const synthetic = rows.filter((row) => row.synthetic).length;
const issues = rows.filter((row) => row.issues.length > 0);

const sections = [];
let currentRegion = "";
for (const row of rows) {
  if (row.region !== currentRegion) {
    currentRegion = row.region;
    sections.push(`## ${currentRegion}`);
  }

  const mark = row.issues.length === 0 ? "x" : " ";
  const suffix = [
    row.country,
    row.source,
    row.synthetic ? "synthetic" : null,
    row.issues.length ? `issues=${row.issues.join(",")}` : null
  ].filter(Boolean).join(" | ");
  sections.push(`- [${mark}] \`${row.id}\` | ${row.title} | ${row.titleEn} | ${suffix}`);
}

const body = [
  "# Story Narrative Audit Checklist",
  "",
  "This file is generated by `scripts/build-story-narrative-checklist.mjs`.",
  "",
  `- Total stories: ${total}`,
  `- Checked stories: ${checked}`,
  `- Manual narratives: ${sources.manual}`,
  `- Curated narratives: ${sources.curated}`,
  `- Generated narratives: ${sources.generated}`,
  `- Synthetic-seed stories audited: ${synthetic}`,
  `- Open issues: ${issues.length}`,
  "",
  ...sections,
  ""
].join("\n");

fs.writeFileSync(outputPath, body);

function readGeneratedNarratives(filePath) {
  if (!fs.existsSync(filePath)) {
    return {};
  }

  const source = fs.readFileSync(filePath, "utf8");
  const match = source.match(/export const storyNarratives:[\s\S]*?=\s*(\{[\s\S]*\}) as const;/);
  if (!match) {
    return {};
  }

  return JSON.parse(match[1]);
}

function splitParagraph(paragraph, locale) {
  const pattern =
    locale === "zh"
      ? /[^。！？!?]+[。！？!?]?/g
      : /[^.!?]+[.!?]+(?:["'”])?|[^.!?]+$/g;

  return (paragraph.match(pattern) ?? [])
    .map((sentence) => sentence.trim())
    .filter(Boolean);
}

function isSyntheticSeed(story) {
  return story.summary.en.includes(" adds a ")
    || story.summary.zh.includes("补入一则")
    || story.background?.en?.includes("broadens the atlas")
    || story.background?.zh?.includes("拓展故事地图");
}
