import fs from "node:fs";
import path from "node:path";

import { loadStories } from "./load-stories.mjs";

const workspaceDir = process.cwd();
const outputPath = path.join(workspaceDir, "docs/synthetic-story-source-plan.md");
const stories = loadStories(workspaceDir);

const syntheticStories = stories
  .filter((story) =>
    story.summary.en.includes(" adds a ")
    || story.summary.zh.includes("补入一则")
    || story.background?.en?.includes("broadens the atlas")
    || story.background?.zh?.includes("拓展故事地图")
  )
  .sort((left, right) =>
    (left.region?.zh ?? "").localeCompare(right.region?.zh ?? "", "zh-Hans")
    || left.country.zh.localeCompare(right.country.zh, "zh-Hans")
    || left.title.zh.localeCompare(right.title.zh, "zh-Hans")
  );

const lines = [
  "# Synthetic Story Source Plan",
  "",
  "This file is generated by `scripts/build-synthetic-source-plan.mjs`.",
  "",
  `- Synthetic stories: ${syntheticStories.length}`,
  "- Goal: keep the current narratives non-boilerplate now, then deepen each entry with better source-backed detail.",
  "",
];

let currentRegion = "";

for (const story of syntheticStories) {
  const region = story.region?.zh ?? "未分区";
  if (region !== currentRegion) {
    currentRegion = region;
    lines.push(`## ${region}`, "");
  }

  const plan = getSourcePlan(story);
  lines.push(`- [x] \`${story.id}\` | ${story.title.zh} | ${story.country.zh} | narrative 已处理`);
  lines.push(`  source next: ${plan.join(" -> ")}`);
  lines.push("");
}

fs.writeFileSync(outputPath, lines.join("\n"));

function getSourcePlan(story) {
  const region = story.region?.en ?? "";
  const country = story.country.en;
  const category = story.category;

  if (region === "Mesoamerica" || ["Mexico", "Guatemala", "El Salvador", "Honduras"].includes(country)) {
    return [
      "official cultural institutions",
      "regional scholarship and open journals",
      "public-domain folklore collections",
      "encyclopedic fallback"
    ];
  }

  if (["Oceania", "Polynesia", "Melanesia"].includes(region) || ["Australia", "New Zealand", "Samoa", "Fiji", "Kiribati", "Micronesia", "Nauru", "French Polynesia"].includes(country)) {
    return [
      "museum and indigenous cultural resources",
      "ethnographic public-domain collections",
      "regional scholarship",
      "encyclopedic fallback"
    ];
  }

  if (region.includes("Africa") || country === "Ethiopia" || country === "Sudan" || country === "South Sudan") {
    return [
      "ethnographic public-domain collections",
      "regional scholarship and oral-tradition studies",
      "museum or cultural institute resources",
      "encyclopedic fallback"
    ];
  }

  if (["Western Europe", "Northern Europe", "Eastern Europe", "Southern Europe", "West Asia", "Western Asia", "South Asia", "East Asia", "Southeast Asia", "Central Asia"].includes(region)) {
    return [
      category === "epic" || category === "myth" ? "public-domain primary texts" : "public-domain folklore collections",
      "scholarly editions or open references",
      "museum or cultural institution resources",
      "encyclopedic fallback"
    ];
  }

  if (region === "South America" || region === "North America" || region === "Central America") {
    return [
      "regional folklore collections",
      "museum or cultural institution resources",
      "open scholarship",
      "encyclopedic fallback"
    ];
  }

  return [
    "public-domain collections",
    "open scholarship",
    "cultural institutions",
    "encyclopedic fallback"
  ];
}
